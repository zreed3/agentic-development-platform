#!/usr/bin/env node
// Generate CLAUDE.md as a mirror of AGENTS.md.
//
// Claude Code reads CLAUDE.md; Codex and other harnesses read AGENTS.md. To keep
// one rulebook instead of two that drift (the F1 anti-pattern from the v0.9.1
// field report), AGENTS.md is the single source of truth and CLAUDE.md is
// generated from it.
//
//   node scripts/adg-claude-md.mjs           -> write CLAUDE.md from AGENTS.md
//   node scripts/adg-claude-md.mjs --check    -> exit 1 if CLAUDE.md is stale
//
// The --check mode is what the conformance doctor / CI calls.

import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const SOURCE = "AGENTS.md";
const TARGET = "CLAUDE.md";

const BANNER = [
  "<!--",
  "  GENERATED FROM AGENTS.md — DO NOT EDIT THIS FILE.",
  "  CLAUDE.md is a generated mirror so Claude Code and Codex read one rulebook.",
  "  Edit AGENTS.md, then run: npm run claude:generate",
  "-->",
  "",
  "",
].join("\n");

function abs(file) {
  return path.join(root, file);
}

function render() {
  const source = fs.readFileSync(abs(SOURCE), "utf8");
  return BANNER + source;
}

const check = process.argv.includes("--check");
const generated = render();

if (check) {
  const current = fs.existsSync(abs(TARGET)) ? fs.readFileSync(abs(TARGET), "utf8") : "";
  if (current !== generated) {
    console.error(
      `${TARGET} is out of sync with ${SOURCE}. Run: npm run claude:generate`,
    );
    process.exit(1);
  }
  console.log(`${TARGET} is in sync with ${SOURCE}.`);
  process.exit(0);
}

fs.writeFileSync(abs(TARGET), generated, "utf8");
console.log(`Wrote ${TARGET} from ${SOURCE} (${generated.length} bytes).`);
